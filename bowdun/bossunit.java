    @Override
    public void hit( weapon w )
    {
        if ( washit || !bai.hitback() ) {
            
            
            
            return;
        
        
        }
        
        bai.freezetimer = 70;
        
        if ( w.getatt() > 0 ) {
            getfig().redit();
        }
        
        current.getweap2().clear();
        
//        
//        
        washit = true;
        life -= 1;
        w.hitit();
        w.onhittrigger();
        if ( life > 100 ) {
            life = 100;
        }
        
        bai.hitthis();
        
        
    }
    
    
    @Override
    public void step( Graphics g )
    {
        
        super.step(g);
        
        
        if ( washit ) {
            
            if ( life % 2 == 0 ) {
                if ( x >= 900 ) {
                    washit = false;
//                    bai.plant();
                    bai.unhit();
                }
            } else {
                if ( x <= 100 ) {
                    washit = false;
//                    bai.plant();
                    bai.unhit();
                }
            }
            
            
        }
        
    }
    
    
    
    
